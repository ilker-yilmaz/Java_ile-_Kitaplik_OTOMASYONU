    private void jButtonKitapEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKitapEkleActionPerformed

        Connection connection=null;
        DbHelper dbHelper=new DbHelper();
        PreparedStatement statement=null;
        try {
            connection = dbHelper.getConnection();
            String sql = "insert into kitap (Kitap Adı,Yayınevi Adı,Yazar Adı,Kitap Dili, Sayfa Sayısı, Yayın Yılı) values(?,?,?,?,?,?)";
            statement = connection.prepareStatement(sql);
            statement.setString(4, txtKitapAdı.getText());
            statement.setString(1, txtYayıneviAdı.getText());
            statement.setString(2, txtYazarAdı.getText());
            statement.setString(3, txtKitapDili.getText());
            statement.setInt(5,Integer.valueOf(txtSayfaSayısı.getText()));
            statement.setInt(6,Integer.valueOf(txtYayınYılı.getText()));
            int result = statement.executeUpdate();

            populateTable();//tablo doldur

        } catch (SQLException exception) {
            dbHelper.showErrorMessage(exception);
        } finally {
            try {
                statement.close();
                connection.close();
            } catch (SQLException ex) {

            }

            }
    }//GEN-LAST:event_jButtonKitapEkleActionPerformed

