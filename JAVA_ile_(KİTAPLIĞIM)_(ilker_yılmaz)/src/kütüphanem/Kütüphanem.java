    public void populateTable() throws SQLException{//tabloDoldur metodumuz
        model=(DefaultTableModel) tblKitaplar.getModel();
        model.setRowCount(0);//iki defa işlem yapmaması için tabloyu bir defa temizliyoruz.
        ArrayList<Kitap> kitaplar = getKitaplar();
        for (Kitap kitap : kitaplar) {
            Object[] row = {kitap.getKitapAdı(), kitap.getYayıneviAdı(), kitap.getYazarAdı(), kitap.getKitapDili(), kitap.getSayfaSayısı(),kitap.getYayınYılı()};
            model.addRow(row);
        }
        
    }
    public ArrayList<Kitap> getKitaplar() throws SQLException {
        Connection connection = null;
        DbHelper dbHelper = new DbHelper();
        Statement statement = null;
        ResultSet resultSet;
        ArrayList<Kitap> kitaplar = null;

        try {
            connection = dbHelper.getConnection();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("select * from kitap");
            kitaplar = new ArrayList<Kitap>();
            while (resultSet.next()) {
                kitaplar.add(new Kitap(
                        resultSet.getInt("Sayfa Sayısı"),
                        resultSet.getString("Yazar Adı"),
                        resultSet.getString("Yayınevi Adı"),
                        resultSet.getString("Kitap Dili"),
                        resultSet.getString("Kitap Adı"),
                        resultSet.getInt("Yayın Yılı")
                        
                ));
            }

        } catch (SQLException exception) {
            dbHelper.showErrorMessage(exception);
        } finally {
            statement.close();
            connection.close();
        }
        return kitaplar;
    }

